# Somar 2 números

print('\033[1;31mPrograma para somar 2 números!\033[m') #Título do programa
num1 = float(input('Digite 1° número: ')) # Solicita ao usuário do 1° número para o usuário
num2 = float(input('Digite 2° número: ')) # Solicita ao usuário do 2° número para o usuário
area = float(num1 + num2) # Efetua a soma dos números inseridos
print(f'A soma dos números digitados: {area}') # Mostra o resultado da soma